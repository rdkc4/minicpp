#ifndef ASM_GENERATOR_HPP
#define ASM_GENERATOR_HPP

#include <fstream>
#include <string>

class AsmGenerator{
    public:
        
        AsmGenerator(std::string& output);

        ~AsmGenerator();

        //-----------------------------------------------------------------------------------------------------------------------------------------------------
        // check if file is successfully opened
        //-----------------------------------------------------------------------------------------------------------------------------------------------------
        bool isOpen();

        //-----------------------------------------------------------------------------------------------------------------------------------------------------
        // start of an asm file
        //-----------------------------------------------------------------------------------------------------------------------------------------------------
        void genStart();

        //-----------------------------------------------------------------------------------------------------------------------------------------------------
        // asm instructions
        //-----------------------------------------------------------------------------------------------------------------------------------------------------

        void genMov(std::string l, std::string r, std::string ext = "");

        void genCmp(std::string l, std::string r);

        void genOperation(std::string op, std::string l, std::string r = "");

        void genLabel(std::string label);

        void genRet();

        void genJmp(std::string jmp, std::string label);

        void genCall(std::string func);

        //-----------------------------------------------------------------------------------------------------------------------------------------------------
        // stack instructions
        //-----------------------------------------------------------------------------------------------------------------------------------------------------

        void genPush(std::string r);

        void genPop(std::string r);

        //-----------------------------------------------------------------------------------------------------------------------------------------------------
        // generate function prologue
        //-----------------------------------------------------------------------------------------------------------------------------------------------------
        void genFuncPrologue();

        //-----------------------------------------------------------------------------------------------------------------------------------------------------
        // generate function epilogue
        //-----------------------------------------------------------------------------------------------------------------------------------------------------
        void genFuncEpilogue();

        //-----------------------------------------------------------------------------------------------------------------------------------------------------
        // system call for exit
        //-----------------------------------------------------------------------------------------------------------------------------------------------------
        void genExit();

        void genNewLine();

    private:

        //-----------------------------------------------------------------------------------------------------------------------------------------------------
        // .s output file for asm code
        //-----------------------------------------------------------------------------------------------------------------------------------------------------
        std::ofstream output;


};

#endif